	AREA RESET, DATA, READONLY
	EXPORT __VECTORS
__VECTORS

	DCD 0x40001000 ;Stack pointer when stack is empty
	DCD Reset_Handler
	ALIGN	
	AREA mycode, CODE, READONLY
	ENTRY
	EXPORT Reset_Handler
Reset_Handler
	LDR R0,=SRC			;LOAD ADDRESS OF SOURCE
	LDR R1,=DST			;LOAD ADDRESS OF DESTINATION
	LDR R3,=10			;INITIALIZE LOOP COUNTER TO 10
UP	LDR R2,[R0],#4		;LOAD DATA POINTED NY R0 INTO R2 USING POST INDEXING
	STR R2,[R1],#4		;STORE DATA INTO REGISTER
	SUBS R3,#1			;DECREMENT COUNTER
	BNE UP
STOP
	B STOP
SRC	DCD 0x1,0x2,0x3,0x4,0x5,0x6,0x7,0x8,0x9,0xA
	AREA mydata, DATA, READWRITE
DST DCD 0x0
	END
	
	
	
	